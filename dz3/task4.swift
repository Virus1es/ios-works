/*
Задание 4

Дана строка, содержащая произвольное значение. Необходимо попытаться преобразовать ее в целое число.

Если преобразование прошло успешно, увеличьте полученное число на 10 и выведите результат. 
Если преобразование невозможно, выведите сообщение об ошибке.

При решении задания запрещается использовать принудительное извлечение значения (оператор !). 
Необходимо использовать безопасные способы работы с опциональными значениями.
*/

func convertAndAddTen(_ input: String) {
    if let number = Int(input) {
        let result = number + 10
        print("Успешно преобразовано. Результат: \(result)")
    } else {
        print("Ошибка: строка \"\(input)\" не является целым числом")
    }
}

convertAndAddTen("42")
convertAndAddTen("-5")
convertAndAddTen("100abc")
convertAndAddTen("3.14")