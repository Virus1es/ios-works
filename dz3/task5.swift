/*
Задание 5

Необходимо реализовать функцию, которая принимает номер дня недели и возвращает его тип.

Если номер дня от 1 до 5 включительно — это рабочий день. Если номер равен 6 или 7 — это выходной. 
Во всех остальных случаях необходимо вернуть сообщение об ошибке.

После реализации функции необходимо вызвать ее в цикле для значений от 1 до 8 и вывести результаты в консоль.
*/

func getDayType(_ day: Int) -> String {
    switch day {
    case 1...5:
        return "Рабочий день"
    case 6...7:
        return "Выходной"
    default:
        return "Ошибка: неверный номер дня недели"
    }
}

print("Проверка дней недели:")
for day in 1...8 {
    print("День \(day): \(getDayType(day))")
}